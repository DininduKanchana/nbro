Air pollution is a health concern that leads to many critical acute and chronic health impacts depending on the exposure levels and pollutant toxicity. Hence, the people living in urban areas have the right to know about the air pollution levels in their surrounding areas. Thereby, with the installed digital display unit, NBRO have created a platform to disseminate daily Air Quality levels with Air Quality Index (with reference to USEPA Guide line) among the public<br><br>
NBRO involves in air quality monitoring for more than 30 years and conducted National Level Monitoring Program during past years. Our service was further extended by starting a continuous monitoring station at The Meteorological Department at Colombo 07, in 2018. The foremost purpose of this program is to disseminate real time air quality conditions to general public. Therefore, the data were published through web sites and mobile apps while this digital display unit was also a part of the project to create awareness on public<br><br>
NBRO wish to establish more of these type digital display panels in the major cities of Sri Lanka in near future to create public awareness on the air they breathe<br><br>

